package zad1;

public class CFConverter {

	/**
	 * Dokonanie konwersji ze skali Celsjusza do Fahrenheita
	 * @param degC stopnie w skali Celsjusza
	 * @return stopnie w skali Fahrenheita
	 */
	public static double convertCelsiusToFahrenheit(double degC) {
			
		return (degC * 9 / 5) + 32;
	}
	
	
	
}